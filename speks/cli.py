@app.command()
def serve(
    project_dir: str = typer.Argument(".", help="Path to the project workspace"),
    port: int = typer.Option(0, "--port", "-p", help="Port (0 = use config)"),
    develop: bool = typer.Option(False, "--develop", "-d", help="Watch source files and rebuild on changes"),
    revisions: int = typer.Option(
        0, "--revisions", "-r",
        help="Number of git revisions (0 = use config)",
    ),
) -> None:
    """Start the dev server (MkDocs site + FastAPI playground API)."""
    import uvicorn

    from speks.core.config import load_config
    from speks.web.builder import build_site
    from speks.web.server import create_app

    root = Path(project_dir).resolve()
    config = load_config(root)
    set_locale(config.locale)

    serve_port = port or config.serve_port

    console.print(t("cli.building_mkdocs"))
    site_dir = build_site(root, revisions_override=revisions or None)

    fastapi_app = create_app(root, site_dir)
    console.print(
        f"[green]✔[/green] {t('cli.serving_at')} [bold]http://localhost:{serve_port}[/bold]  {t('cli.stop_hint')}"
    )

    if develop:
        _serve_with_watch(fastapi_app, root, config, serve_port, revisions)
    else:
        uvicorn.run(fastapi_app, host="0.0.0.0", port=serve_port, log_level="warning")


def _serve_with_watch(
    fastapi_app: Any,
    root: Path,
    config: Any,
    serve_port: int,
    revisions: int,
) -> None:
    """Run uvicorn in a thread and watch for file changes to trigger rebuilds."""
    import threading

    import uvicorn
    from watchfiles import DefaultFilter, watch

    from speks.core.config import ProjectConfig
    from speks.web.builder import build_site

    cfg = config if isinstance(config, ProjectConfig) else None
    assert cfg is not None

    # Directories to watch
    watch_paths: list[Path] = []
    for d in (cfg.src_dir, cfg.docs_dir, cfg.diagrams_dir):
        p = root / d
        if p.is_dir():
            watch_paths.append(p)
    toml_path = root / "speks.toml"
    if toml_path.exists():
        watch_paths.append(toml_path)

    # Files generated by the build process that live inside watched dirs.
    # Ignore them to avoid an infinite rebuild loop.
    _GENERATED_ASSETS = {"logo.svg", "logo-white.svg", "speks.css"}
    docs_assets_dir = str((root / cfg.docs_dir / "assets").resolve())
    _default_filter = DefaultFilter()

    def _watch_filter(change: Any, path: str) -> bool:
        """Ignore build-generated assets inside docs/assets/."""
        if not _default_filter(change, path):
            return False
        if path.startswith(docs_assets_dir) and Path(path).name in _GENERATED_ASSETS:
            return False
        return True

    # Start uvicorn in a daemon thread
    server = uvicorn.Server(uvicorn.Config(
        fastapi_app, host="0.0.0.0", port=serve_port, log_level="warning",
    ))
    thread = threading.Thread(target=server.run, daemon=True)
    thread.start()

    console.print(
        f"[dim]{t('cli.watching', dirs=', '.join(str(p.relative_to(root)) for p in watch_paths))}[/dim]"
    )

    try:
        for changes in watch(*watch_paths, watch_filter=_watch_filter):
            changed = {Path(c[1]).name for c in changes}
            console.print(
                f"[yellow]↻[/yellow] {t('cli.rebuilding')} [dim]({', '.join(sorted(changed))})[/dim]"
            )
            try:
                build_site(root, revisions_override=revisions or None)
                console.print(f"[green]✔[/green] {t('cli.rebuild_done')}")
            except Exception as exc:
                err_console.print(f"[red]✘[/red] {t('cli.rebuild_error', error=exc)}")
    except KeyboardInterrupt:
        server.should_exit = True
        thread.join(timeout=3)


