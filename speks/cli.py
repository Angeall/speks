@app.command()
def init(
    name: str = typer.Argument("my-analysis-project", help="Project directory name"),
) -> None:
    """Scaffold a new Speks analysis workspace."""
    root = Path.cwd() / name
    if root.exists():
        console.print(f"[red]{t('cli.dir_exists', name=name)}[/red]")
        raise typer.Exit(1)

    (root / "src").mkdir(parents=True)
    (root / "docs").mkdir(parents=True)
    (root / "diagrams").mkdir(parents=True)

    # Make src a proper Python package so relative imports work
    (root / "src" / "__init__.py").touch()

    (root / "speks.toml").write_text(
        _SAMPLE_TOML.format(name=name), encoding="utf-8"
    )
    (root / "mkdocs.yml").write_text(
        _SAMPLE_MKDOCS_YML.format(name=name), encoding="utf-8"
    )
    (root / "src" / "rules.py").write_text(_SAMPLE_RULE, encoding="utf-8")
    (root / "docs" / "index.md").write_text(_SAMPLE_INDEX, encoding="utf-8")
    (root / "docs" / "credit-rules.md").write_text(_SAMPLE_DOC, encoding="utf-8")
    (root / "diagrams" / "sequence.puml").write_text(_SAMPLE_PUML, encoding="utf-8")

    # Copy packaged logos into the new project's docs/assets/
    from speks.web.builder import _get_packaged_asset

    import shutil

    assets_dir = root / "docs" / "assets"
    assets_dir.mkdir(parents=True, exist_ok=True)
    for logo_name in ("logo.svg", "logo-white.svg"):
        logo = _get_packaged_asset(logo_name)
        if logo is not None:
            shutil.copy2(logo, assets_dir / logo_name)

    console.print(f"[green]✔[/green] {t('cli.workspace_created')} [bold]{root}[/bold]")
    console.print(f"  {t('cli.run_hint')} [bold]cd {name} && speks serve[/bold]")


